        <div>
            Satya Nadella became the CEO of Microsoft on February 4, 2014, succeeding Steve Ballmer. Under Nadella's leadership, Microsoft experienced significant growth and transformation, marking a departure from its traditional focus on Windows and Office products. Here are some key aspects of Microsoft's growth under Satya Nadella:
        </div>
        <div class="intro">
            <h3>Cloud Computing and Azure:</h3>
            <p>One of the major shifts under Nadella's leadership was a strong emphasis on cloud computing. Microsoft Azure, the company's cloud platform, saw tremendous growth. Nadella played a key role in positioning Microsoft as a leader in the cloud services market, competing with companies like Amazon Web Services (AWS) and Google Cloud.</p>
        </div>
        <div class="intro">
            <h3>Productivity and Business Solutions:</h3>
            <p>Nadella focused on productivity and business solutions, emphasizing products like Office 365 and Microsoft 365. These platforms evolved to include cloud-based collaboration tools, enhancing communication and collaboration for businesses.</p>
        </div>
        <div class="intro">
            <h3>Strategic Acquisitions:</h3>
            <p>Microsoft made several strategic acquisitions under Nadella, including the purchase of LinkedIn in 2016 and GitHub in 2018. These acquisitions strengthened Microsoft's position in professional networking and software development, respectively.</p>
        </div>
        <div class="intro">
            <h3>Focus on Artificial Intelligence (AI) and Mixed Reality:</h3>
            <p>Nadella emphasized the importance of artificial intelligence and mixed reality technologies. Microsoft invested in AI research and development, integrating AI capabilities into its products and services. The HoloLens, a mixed reality headset, was also developed under Nadella's leadership.</p>
        </div>
        <div class="intro">
            <h3>Open Source Initiatives:</h3>
            <p>Microsoft, traditionally known for its proprietary software, adopted a more open approach under Nadella. The company embraced open-source technologies and became an active contributor to open-source projects, fostering collaboration within the developer community.</p>
        </div>
        <div class="intro">
            <h3>Windows 10 and Windows as a Service:</h3>
            <p>Nadella oversaw the launch of Windows 10, which introduced a more agile development model with continuous updates and improvements. This shift towards "Windows as a Service" allowed Microsoft to deliver regular updates and new features to users.</p>
        </div>
        <div class="intro">
            <h3>Diversification of Revenue Streams:</h3>
            <p>Under Nadella, Microsoft diversified its revenue streams beyond traditional software licensing. The success in cloud services, subscription-based models, and hardware (such as Surface devices) contributed to a more balanced and resilient financial profile.</p>
        </div>
        <div class="intro">
            <h3>Cultural Transformation:</h3>
            <p>Nadella also led a cultural transformation within Microsoft, emphasizing a growth mindset and fostering a more inclusive and collaborative workplace. This cultural shift aimed to encourage innovation and adaptability within the company.</p>
        </div>
